import { describe, expect, it, vi } from 'vitest';

vi.mock('@/backend/prompts/branch-rename', () => ({
  buildBranchRenameInstruction: vi.fn(),
}));

vi.mock('@/backend/prompts/workflows', () => ({
  getWorkflowContent: vi.fn(),
}));

vi.mock('@/backend/services/logger.service', () => ({
  createLogger: () => ({
    debug: vi.fn(),
    info: vi.fn(),
    warn: vi.fn(),
    error: vi.fn(),
  }),
}));

import { buildBranchRenameInstruction } from '@/backend/prompts/branch-rename';
import { getWorkflowContent } from '@/backend/prompts/workflows';
import { buildChildWorkspaceContext, SessionPromptBuilder } from './session.prompt-builder';

describe('buildChildWorkspaceContext', () => {
  it('builds the exact child context with parent details and report-back criteria', () => {
    expect(
      buildChildWorkspaceContext({
        parentWorkspaceName: 'parent-workspace',
        parentProjectName: 'parent-project',
        reportBackOn: 'a PR is opened',
      })
    ).toBe(
      `## Child Workspace Context\n` +
        `You are working in a child workspace created by the parent workspace "parent-workspace" (project: parent-project). ` +
        `When you have completed your task or reached a significant milestone — especially if you produced a PR, a finding, or are blocked — ` +
        `use the \`send_message_to_parent\` tool to report back. Include a brief summary of what was done and any next steps the parent workspace should be aware of.\n` +
        `Report back when: a PR is opened\n`
    );
  });

  it('uses parent-name fallbacks and omits absent report-back criteria', () => {
    expect(
      buildChildWorkspaceContext({
        parentWorkspaceName: null,
        parentProjectName: null,
      })
    ).toBe(
      `## Child Workspace Context\n` +
        `You are working in a child workspace created by the parent workspace "unknown" (project: unknown project). ` +
        `When you have completed your task or reached a significant milestone — especially if you produced a PR, a finding, or are blocked — ` +
        `use the \`send_message_to_parent\` tool to report back. Include a brief summary of what was done and any next steps the parent workspace should be aware of.\n`
    );
  });

  it('omits whitespace-only report-back criteria', () => {
    const context = buildChildWorkspaceContext({ reportBackOn: '  \n\t' });

    expect(context).not.toContain('Report back when:');
  });

  it('trims surrounding whitespace from report-back criteria', () => {
    const context = buildChildWorkspaceContext({ reportBackOn: '  a PR is opened  ' });

    expect(context).toContain('\nReport back when: a PR is opened\n');
    expect(context).not.toContain('  a PR is opened  ');
  });
});

describe('SessionPromptBuilder', () => {
  describe('shouldInjectBranchRename', () => {
    const builder = new SessionPromptBuilder();

    it('returns true when branch is auto-generated and no sessions have run', () => {
      expect(
        builder.shouldInjectBranchRename({
          branchName: 'auto-branch',
          isAutoGeneratedBranch: true,
          hasHadSessions: false,
        })
      ).toBe(true);
    });

    it('returns false when workspace has already had sessions', () => {
      expect(
        builder.shouldInjectBranchRename({
          branchName: 'auto-branch',
          isAutoGeneratedBranch: true,
          hasHadSessions: true,
        })
      ).toBe(false);
    });

    it('returns false when branch is not auto-generated', () => {
      expect(
        builder.shouldInjectBranchRename({
          branchName: 'feature/my-branch',
          isAutoGeneratedBranch: false,
          hasHadSessions: false,
        })
      ).toBe(false);
    });

    it('returns false when branch name is missing', () => {
      expect(
        builder.shouldInjectBranchRename({
          branchName: null,
          isAutoGeneratedBranch: true,
          hasHadSessions: false,
        })
      ).toBe(false);
    });

    it('returns false when isAutoGeneratedBranch is undefined', () => {
      expect(
        builder.shouldInjectBranchRename({
          branchName: 'some-branch',
          isAutoGeneratedBranch: undefined,
          hasHadSessions: false,
        })
      ).toBe(false);
    });

    it('returns false when hasHadSessions is undefined (safer default for older workspaces)', () => {
      expect(
        builder.shouldInjectBranchRename({
          branchName: 'auto-branch',
          isAutoGeneratedBranch: true,
          hasHadSessions: undefined,
        })
      ).toBe(false);
    });
  });

  it('injects branch rename instruction for auto-generated branches on first session', () => {
    vi.mocked(getWorkflowContent).mockReturnValue('workflow');
    vi.mocked(buildBranchRenameInstruction).mockReturnValue('rename');

    const builder = new SessionPromptBuilder();
    const result = builder.buildSystemPrompt({
      workflow: 'ci-fix',
      workspace: {
        branchName: 'auto-branch',
        isAutoGeneratedBranch: true,
        hasHadSessions: false,
        name: 'Workspace A',
        description: 'desc',
      },
      project: { githubOwner: 'owner' },
    });

    expect(result.systemPrompt).toBe('renameworkflow');
    expect(result.workflowPrompt).toBe('workflow');
    expect(result.injectedBranchRename).toBe(true);
  });

  it('uses workflow prompt without branch rename when branch is not auto-generated', () => {
    vi.mocked(getWorkflowContent).mockReturnValue('workflow');

    const builder = new SessionPromptBuilder();
    const result = builder.buildSystemPrompt({
      workflow: 'ci-fix',
      workspace: {
        branchName: 'feature/real',
        isAutoGeneratedBranch: false,
        hasHadSessions: false,
        name: 'Workspace B',
        description: null,
      },
      project: { githubOwner: 'owner' },
    });

    expect(result.systemPrompt).toBe('workflow');
    expect(result.workflowPrompt).toBe('workflow');
    expect(result.injectedBranchRename).toBe(false);
  });

  it('returns undefined system prompt when workflow prompt is missing', () => {
    vi.mocked(getWorkflowContent).mockReturnValue(null);

    const builder = new SessionPromptBuilder();
    const result = builder.buildSystemPrompt({
      workflow: 'missing',
      workspace: {
        branchName: null,
        isAutoGeneratedBranch: false,
        name: 'Workspace C',
        description: null,
      },
      project: null,
    });

    expect(result.systemPrompt).toBeUndefined();
    expect(result.workflowPrompt).toBeUndefined();
    expect(result.injectedBranchRename).toBe(false);
  });

  it('does not inject branch rename when isAutoGeneratedBranch is undefined', () => {
    vi.mocked(getWorkflowContent).mockReturnValue('workflow');

    const builder = new SessionPromptBuilder();
    const result = builder.buildSystemPrompt({
      workflow: 'ci-fix',
      workspace: {
        branchName: 'some-branch',
        // isAutoGeneratedBranch not set (undefined)
        name: 'Workspace D',
        description: null,
      },
      project: { githubOwner: 'owner' },
    });

    expect(result.systemPrompt).toBe('workflow');
    expect(result.workflowPrompt).toBe('workflow');
    expect(result.injectedBranchRename).toBe(false);
  });

  it('does not inject branch rename when workspace has already had sessions', () => {
    vi.mocked(getWorkflowContent).mockReturnValue('workflow');

    const builder = new SessionPromptBuilder();
    const result = builder.buildSystemPrompt({
      workflow: 'ci-fix',
      workspace: {
        branchName: 'auto-branch',
        isAutoGeneratedBranch: true,
        hasHadSessions: true,
        name: 'Workspace F',
        description: 'desc',
      },
      project: { githubOwner: 'owner' },
    });

    expect(result.systemPrompt).toBe('workflow');
    expect(result.workflowPrompt).toBe('workflow');
    expect(result.injectedBranchRename).toBe(false);
  });

  it('uses default Claude prompt for regular user sessions', () => {
    vi.mocked(getWorkflowContent).mockReturnValue('workflow');

    const builder = new SessionPromptBuilder();
    const result = builder.buildSystemPrompt({
      workflow: 'followup',
      workspace: {
        branchName: null,
        isAutoGeneratedBranch: false,
        name: 'Workspace E',
        description: null,
      },
      project: null,
    });

    expect(result.systemPrompt).toBeUndefined();
    expect(result.workflowPrompt).toBeUndefined();
    expect(result.injectedBranchRename).toBe(false);
  });
});
