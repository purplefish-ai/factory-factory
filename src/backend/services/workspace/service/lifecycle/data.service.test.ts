import { beforeEach, describe, expect, it, vi } from 'vitest';

const mockUpdate = vi.hoisted(() => vi.fn());

vi.mock('@/backend/services/workspace/resources/workspace.accessor', () => ({
  workspaceAccessor: {
    update: (...args: unknown[]) => mockUpdate(...args),
  },
}));

import { workspaceDataService } from './data.service';

describe('workspaceDataService', () => {
  beforeEach(() => {
    vi.clearAllMocks();
  });

  it('sets the branch name and clears the auto-generated flag in one update', async () => {
    mockUpdate.mockResolvedValue({ id: 'workspace-1' });

    expect(workspaceDataService.setBranchNameAndClearAutoGenerated).toBeTypeOf('function');
    await workspaceDataService.setBranchNameAndClearAutoGenerated('workspace-1', 'my-custom-name');

    expect(mockUpdate).toHaveBeenCalledOnce();
    expect(mockUpdate).toHaveBeenCalledWith('workspace-1', {
      branchName: 'my-custom-name',
      isAutoGeneratedBranch: false,
    });
  });
});
